static double
GetBlerHARQ (double snr)
{
	int blerHARQ = 0;
	int index = -1;
	double R = 0.0;

	if(FrameManager::Init()->GetHARQ()){

		if ( snr <= SNR4Harq [0] )	{

				blerHARQ = BLER4Harq[0]; // perchè 1?

		} else if (snr >= SNR4Harq [40]){

			  	blerHARQ = BLER4Harq [40]; // perchè 0?

		} else {

		      for (int i = 0; i < 40; i++){
		          if (snr >= SNR4Harq [i] && snr < SNR4Harq [i+1])
		            {
		              index = i;
		            }
		      }
		}

		  if (index != -1)
		    {
		      R = (snr - SNR4Harq [index]) / ( SNR4Harq [index + 1] - SNR4Harq [index] );
		      blerHARQ = BLER4Harq [index] + R * ( BLER4Harq [index + 1] - BLER4Harq [index] );
		    }
	}
	else{
			cout << "HARQ is not properly configured. CRITICAL ERROR"<< endl;
			exit(1)	;
	}
	blerHARQ = ceil(blerHARQ); // intero superiore
	;
	return blerHARQ;
}

